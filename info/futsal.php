				<p>
					Peraturan pertandingan sebagian mengacu pada peraturan PSSI (Persatuan Sepak Bola Seluruh Indonesia).
				</p>
				<p>
					<strong>Sistem Pertandingan</strong> <br>
					Pertandingan dengan sistem gugur untuk putra dan sistem pool untuk putri.
				</p>
				<p>
					<strong>Peserta</strong> <br>
					Setiap sekolah paling banyak mendaftarkan 12 pemain (termasuk kiper) berumur maksimal 18 tahun.<br>
					Jumlah pemain di lapangan adalah 5 orang (termasuk kiper). <br>
					Pergantian pemain tidak dibatasi, namun pemain yang diganti harus keluar lapangan terlebih dahulu. <br>
					Jika pemain pengganti masuk lapangan terlebih dahulu, akan mendapatkan hukuman kartu kuning. <br>
					Maksimal pelanggaran adalah 5 kali. Jika lebih akan dikenakan penalti titik kedua. <br>
					Target peserta adalah 16 tim putra dan 8 tim putri, jika tidak mencapai target maka lomba ditiadakan.
				</p>
				<p>
					<strong>Perlengkapan</strong> <br>
					Pemain wajib memakai seragam baik baju (bernomor punggung) maupun celana. <br>
					Kiper mengenakan seragam yang berbeda dengan pemain. <br>
					Pemain wajib memakai sepatu khusus futsal (tanpa pool) dan diwajibkan memakai dekker. <br>
					Pemain dilarang memakai suatu benda yang dapat membahayakan diri sendiri maupun pemain lainnya.
				</p>
				<p>
					<strong>Lama Pertandingan</strong> <br>
					Waktu pertandingan adalah 2 (babak) x 15 menit, dengan istirahat 5 menit.
				</p>
				<p>
					<strong>Timeout</strong> <br>
					Setiap tim diperbolehkan meminta time out sebanyak 1x di setiap babak dengan durasi 1 menit.
				</p>
				<p>
					<strong>Kartu Kuning dan Kartu Merah</strong> <br>
					Setiap pemain yang dikenai kartu kuning akan mendapat denda sebesar Rp 15.000,- dan untuk kartu merah sebesar Rp 25.000,-.
				<p>
					<strong>Pertandingan Tunda</strong> <br>
					Apabila pertandingan tidak dapat dilanjutkan dan terpaksa ditunda, maka pertandingan akan dilanjutkan <br> 
					pada hari berikutnya dengan waktu ditentukan oleh panitia.
				</p>
				<p>
					<strong>Kehadiran</strong> <br>
					Apabila tim terlambat hadir lebih dari 10 menit dari jadwal yang telah ditentukan <br>
					maka tim tersebut dinyatakan kalah WO (Walk Out) dengan skor 3-0 untuk tim yang telah hadir.
				</p>
				<p>
					<strong>Juara</strong> <br>
					Juara terdiri dari juara 1, 2, dan 3.
